# list
my_list = [1, 2, 3, 4, 5, 6, 7, 8]

for i in my_list:
    print(i)
print("-------------------------")
# Tuple
my_tuple = (1, 2, 3, 4, 5, 6, 7)
for i in my_tuple:
    print(i)
print("-------------------------")

# Dict
my_dict = {'name': 'saynam', 'age': 30, 'city': 'pune'}
for key in my_dict:
    print(key, my_dict[key])

for key, value in my_dict.items():
    print(key, value)
print("-------------------------")

# Sets
my_set = {1, 2, 3, 4, 5}
for i in my_set:
    print(i)
