# break
for i in range(5):
    if i == 3:
        break
    print(i)
print("-------------------------------------------")


# continue
for i in range(5):
    if i == 3:
        continue
    print(i)
print("-------------------------------------------")

# pass
for i in range(5):
    if i == 2:
        pass
    else:
        print(i)
