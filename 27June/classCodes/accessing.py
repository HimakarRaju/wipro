# For accessing
my_list = [1, 2, 3, 4, 5, 6, 7]
print(my_list[0])
print(my_list[-1])

my_tuple = (1, 'hi', 'bye', 4, 5)
print(my_tuple[2])

my_dict = {'name': 'Saynam', 'Age': '30', 'city': 'pune'}
print(my_dict['city'])

