# slicing
# seq[start:stop:step]

my_list = [1, 2, 3, 4, 5, 6, 7]
# Basic slicing
print(my_list[1:4])

print(my_list[::2])

print(my_list[-3:])

# Tuples
my_tuple = (1, 2, 3, 4, 5, 6, 7)

print(my_tuple[1:4])
print(my_tuple[::2])
print(my_tuple[-3:])


#slicing is not possible on dictionaries and sets